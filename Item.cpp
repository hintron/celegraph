#include "Item.h"
#include <string>

//------------------------------------------------------------------------------------------------//
//-------------------                     Class Constructors                   -------------------//
//------------------------------------------------------------------------------------------------//

Item::Item(
    int id,
    std::string name,
    std::string location,
    std::string weight,
    bool equip,
    bool use,
    int x,
    int y
) {
    this->id = id;
    this->name = name;
    this->location = location;
    this->weight = weight;
    this->equip = equip;
    this->use = use;
    this->x = x;
    this->y = y;
}

Item::~Item() {
    return;
}

//------------------------------------------------------------------------------------------------//
//-------------------                      Getter Functions                    -------------------//
//------------------------------------------------------------------------------------------------//
std::string Item::GetName() {
    return name;
}

